#include "../include/mylib.h"

int	ft_toupper(int c)
{
	if (c >= 97 && c <= 122)
	{
		return (c - 32);
	}
	return (c);
}
/*
* Takes an int (c)
* Makes it uppercase
* Returns (c)
*/
