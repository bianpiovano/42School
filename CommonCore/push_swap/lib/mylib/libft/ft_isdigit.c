#include "../include/mylib.h"

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
	{
		return (1);
	}
	return (0);
}
/*
* Takes an int (c)
* Returns 1 if is a digit
* Returns 0 if it is not 
*/