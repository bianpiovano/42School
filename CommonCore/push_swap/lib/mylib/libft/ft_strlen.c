#include "../include/mylib.h"

size_t	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (*str)
	{
		i++;
		str++;
	}
	return (i);
}
/*
* Takes an string
* Counts string length
* Returns string length
* Does not count the null
* character at the end
*/